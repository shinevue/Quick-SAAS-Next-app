export async function getStaticProps() {
	const getHomePageContent = gql`
		{
			pages(where: {id: 121}) {
				edges {
					node {
						featuresPage {
							heroSection {
								title
								subtitle
								backgroundImage {
									sourceUrl
								}
							}
							featuresBanner {
								text
								icons {
									sourceUrl
								}
							}
							ctaBannerTwo {
								title
								paragraph
								buttonLink {
									url
									title
									target
								}
							}
							stats {
								title
								subtitle
								beforeTopText
								beforeNumber
								beforeBottomText
								afterTopText
								afterNumber
								afterBottomText
							}
							threeGridContent {
								title
								paragraph
								buttonLink {
									url
									title
									target
								}
								gridContent {
									title
									paragraph
									icon {
										sourceUrl
									}
								}
							}
						}
					}
				}
			}
		}
	`;

	const response = await client.query({
		query: getHomePageContent,
	});

	return {
		props: {
			featurePageContent: response.data?.pages?.edges[0]?.node?.featuresPage,
		},
	};
}
